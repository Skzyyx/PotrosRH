    private void llenarTabla() {
        try {
            EmpleadoFiltroDTO filtro = new EmpleadoFiltroDTO();
            switch (boxFiltro.getSelectedItem().toString()) {
                case "RFC":
                    filtro.setRfc(tfBuscador.getText());
                    break;
                case "CORREO":
                    filtro.setEmail(tfBuscador.getText());
                    break;
                case "TELEFONO":
                    filtro.setTelefono(tfBuscador.getText());
                    break;
                default:
                    break;
            }

            List<EmpleadoDTO> empleados = ControlRegistro.getInstance().obtenerTodosSinContrato(filtro);
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

            // Limpiar filas anteriores
            modelo.setRowCount(0);

            // Agregar nuevas filas
            for (EmpleadoDTO e : empleados) {
                modelo.addRow(new Object[]{
                    String.join(" ", e.getNombre(), e.getApellidoPaterno(), e.getApellidoMaterno()),
                    e.getRfc(),
                    e.getEmail(),
                    e.getTelefono()
                });
            }
        } catch (PresentacionException ex) {
            Logger.getLogger(SeleccionarEmpleadoContrato.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void cargarListeners() {
        tfBuscador.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                llenarTabla();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                llenarTabla();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                llenarTabla();
            }
        });

        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                // Para evitar que se dispare dos veces (por ajuste interno del evento)
                if (e.getValueIsAdjusting()) {
                    return;
                }

                int filaSeleccionada = jTable1.getSelectedRow();
                if (filaSeleccionada == -1) {
                    return;
                }

                EmpleadoDTO rfc = new EmpleadoDTO();
                rfc.setRfc(String.valueOf(jTable1.getValueAt(filaSeleccionada, 1)));
                try {
                    EmpleadoDTO candidato = ControlRegistro.getInstance().obtenerEmpleado(rfc);

                    lblRfc.setText(candidato.getRfc());
                    lblNombreCompleto.setText(String.join(" ", candidato.getNombre(), candidato.getApellidoPaterno(), candidato.getApellidoMaterno()));
                    lblFechaNacimiento.setText(candidato.getFechaNacimiento().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                    lblTelefono.setText(candidato.getTelefono());
                    lblEmail.setText(candidato.getEmail());
                    String direccion = String.join(" ", candidato.getCalle(), candidato.getNumero(), candidato.getColonia());
                    lblDireccion.setText("<html><div style='width: 249px;'>"
                            + direccion
                            + "</div></html>");

                } catch (PresentacionException ex) {
                    Logger.getLogger(SeleccionarEmpleadoContrato.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        });

        boxFiltro.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                llenarTabla();
            }
        });
    }

    private void btnVolver() {
        ControlFlujo.mostrarMenuContrataciones();
        limpiarCampos();
        llenarTabla();
    }

    private void btnSiguiente() {
        if (jTable1.getSelectedRow() == -1) {
            OptionPane.OptionPane.showErrorMessage(this, "Debes seleccionar un empleado primero.", "Error");
            return;
        }
        EmpleadoDTO empleado = new EmpleadoDTO();
        empleado.setRfc(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
        try {
            empleado = ControlRegistro.getInstance().obtenerEmpleado(empleado);
            System.out.println(empleado.toString());
            ControlFlujo.mostrarCapturarDatosContrato(empleado);
            limpiarCampos();
        } catch (PresentacionException ex) {
            Logger.getLogger(SeleccionarEmpleadoContrato.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void limpiarCampos() {

        boxFiltro.setSelectedIndex(0);
        tfBuscador.setText(" ");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        jTable1.setModel(model);
        lblRfc.setText(" ");
        lblNombreCompleto.setText(" ");
        lblDireccion.setText(" ");
        lblTelefono.setText(" ");
        lblFechaNacimiento.setText(" ");
        lblEmail.setText(" ");
    }
